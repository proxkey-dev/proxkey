#!/usr/bin/env node

/**
 * Production build script for password reset page
 * Optimizes the reset page for production deployment
 */

import fs from 'fs'
import path from 'path'
import { fileURLToPath } from 'url'

const __filename = fileURLToPath(import.meta.url)
const __dirname = path.dirname(__filename)

const RESET_DIR = path.join(__dirname, '../public/auth/reset')
const INDEX_FILE = path.join(RESET_DIR, 'index.html')
const CONFIG_FILE = path.join(RESET_DIR, 'config.prod.js')

console.log('🔧 Building password reset page for production...')

// Read the current index.html
let indexContent = fs.readFileSync(INDEX_FILE, 'utf8')

// Production optimizations
const optimizations = [
  // Remove debug comments
  {
    from: /\/\*[\s\S]*?\*\//g,
    to: '',
  },
  // Minify inline CSS (basic)
  {
    from: /\s+/g,
    to: ' ',
  },
  // Remove console.log statements in production
  {
    from: /console\.log\([^)]*\);?\s*/g,
    to: '',
  },
  // Optimize script loading
  {
    from: "import { createClient } from 'https://esm.sh/@supabase/supabase-js@2'",
    to: "import { createClient } from 'https://esm.sh/@supabase/supabase-js@2.57.4'",
  },
]

// Apply optimizations
optimizations.forEach((opt) => {
  indexContent = indexContent.replace(opt.from, opt.to)
})

// Write optimized file
fs.writeFileSync(INDEX_FILE, indexContent)

// Create production config with environment variables
const prodConfig = `// Production configuration for password reset page
// Auto-generated by build script

window.SUPABASE_CONFIG = {
    // Environment variables (set by deployment platform)
    SUPABASE_URL: window.SUPABASE_URL || process.env.VITE_SUPABASE_URL || '',
    SUPABASE_ANON_KEY: window.SUPABASE_ANON_KEY || process.env.VITE_SUPABASE_ANON_KEY || '',
    
    // Production settings
    PRODUCTION: true,
    DEBUG: false,
    
    // Security settings
    MAX_RESET_ATTEMPTS: parseInt(process.env.VITE_MAX_RESET_ATTEMPTS || '3'),
    RESET_ATTEMPT_WINDOW: parseInt(process.env.VITE_RESET_ATTEMPT_WINDOW || '900000'),
    PASSWORD_MIN_LENGTH: parseInt(process.env.VITE_PASSWORD_MIN_LENGTH || '8'),
    PASSWORD_MAX_LENGTH: parseInt(process.env.VITE_PASSWORD_MAX_LENGTH || '128'),
    
    // Rate limiting
    RATE_LIMIT_ENABLED: true,
    
    // Logging
    LOG_LEVEL: process.env.VITE_LOG_LEVEL || 'warn',
    
    // Redirects
    SUCCESS_REDIRECT: process.env.VITE_SUCCESS_REDIRECT || '/?message=password-reset-success',
    ERROR_REDIRECT: process.env.VITE_ERROR_REDIRECT || '/?error=password-reset-failed'
};

// Make configuration available globally
window.SUPABASE_URL = window.SUPABASE_CONFIG.SUPABASE_URL;
window.SUPABASE_ANON_KEY = window.SUPABASE_CONFIG.SUPABASE_ANON_KEY;

// Production error handling
if (!window.SUPABASE_URL || !window.SUPABASE_ANON_KEY) {
    console.error('❌ Supabase configuration missing in production!');
    if (typeof window !== 'undefined') {
        document.body.innerHTML = '<div style="text-align: center; padding: 50px; font-family: Arial, sans-serif;"><h1>Configuration Error</h1><p>Please contact support.</p></div>';
    }
}
`

fs.writeFileSync(CONFIG_FILE, prodConfig)

// Create a simple health check endpoint
const healthCheck = `<!DOCTYPE html>
<html>
<head>
    <title>Password Reset Health Check</title>
</head>
<body>
    <h1>Password Reset Service</h1>
    <p>Status: OK</p>
    <p>Version: 1.0.0</p>
    <p>Environment: Production</p>
</body>
</html>`

fs.writeFileSync(path.join(RESET_DIR, 'health.html'), healthCheck)

console.log('✅ Production build complete!')
console.log('📁 Files generated:')
console.log('  - index.html (optimized)')
console.log('  - config.prod.js (environment-aware)')
console.log('  - health.html (health check)')
console.log('  - _headers (security headers)')
console.log('  - _redirects (URL redirects)')
console.log('')
console.log('🚀 Ready for deployment!')
console.log('📖 See PRODUCTION_DEPLOYMENT.md for deployment instructions.')
